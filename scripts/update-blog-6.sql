<p>Najlepší svadobný playlist nie je 200 skladieb „všetko čo máme radi“. Je to štruktúra večera + pár pevných momentov + dôvera v DJ-a. Keď dáte DJ-ovi príliš pevnú mriežku, parket často zomrie – keď mu nedáte nič, riskujete skladby, ktoré nechcete.</p>

<figure><img src="https://images.unsplash.com/photo-1514525253161-7a46d19cd819?auto=format&fit=crop&w=1200&q=80" alt="Hudba a párty" /><figcaption>Dobrý playlist je mapa večera, nie zoznam 200 povinných trackov.</figcaption></figure>

<h2>Rozdeľte večer na bloky</h2>
<ol>
<li><strong>Príchod / welcome drink</strong> – jemnejšie, ambient, jazz, acoustic pop</li>
<li><strong>Večera</strong> – tichší background, hlasitosť nesmie prebíjať rozhovory</li>
<li><strong>Prvý tanec a rodinné tance</strong> – vopred dohodnuté skladby</li>
<li><strong>Otvorenie parkietu</strong> – známe, tanečné hity naprieč generáciami</li>
<li><strong>Peak night</strong> – energickejšie, aktuálne aj evergreeny</li>
<li><strong>Záver</strong> – pomalšie, emotívne, „last dance“</li>
</ol>

<h2>Must-play a do-not-play</h2>
<p>Pripravte:</p>
<ul>
<li>10–15 skladieb, ktoré <strong>musia</strong> zaznieť</li>
<li>zoznam, ktorý <strong>nechcete</strong> (ex partner songs, meme tracky, príliš agresívny žáner)</li>
<li>špeciálne momenty: torta, súťaž, odchod</li>
</ul>
<p>Viac než 40 „povinných“ skladieb viaže DJ-ovi ruky a parkiet často zomrie. Menej je viac – a zvyšok nechajte na situáciu.</p>

<figure><img src="https://images.unsplash.com/photo-1429962714451-bb934ecdc4ec?auto=format&fit=crop&w=1200&q=80" alt="Tanečný parket" /><figcaption>Parket rozhoduje: DJ musí vedieť reagovať, nie len prehrávať zoznam.</figcaption></figure>

<h2>Myslite na vek hostí</h2>
<p>Ak sú na svadbe rodičia aj 20-roční kamaráti, striedajte éry. Klasika 80s/90s/00s často naplní parket rýchlejšie než len aktuálny TikTok hit. Dobrý DJ vie prepnúť za 30 sekúnd – ak mu to dovolíte.</p>

<h2>Prvý tanec: praktické tipy</h2>
<ul>
<li>Vyberte skladbu, v ktorej sa cítite prirodzene (nie čo „má byť“)</li>
<li>Skúste si tanec aspoň raz nanečisto</li>
<li>Dajte DJ-ovi čistú verziu / timestamp, ak chcete skrátiť intro</li>
<li>Dohodnite signál, kedy má začať (po príhovore, po prípitku…)</li>
</ul>

<h2>Nechajte priestor improvizácii</h2>
<p>DJ vidí, čo funguje naživo. Ak pevným zoznamom „zamknete“ celý večer, prídete o momenty, keď hostia začnú tancovať na niečo neočakávané. Najlepší výsledok: jasný brief + flexibilný profesionál.</p>

<h2>Mini šablóna, ktorú pošlite DJ-ovi</h2>
<ol>
<li>Vekové zloženie hostí (približne)</li>
<li>Must-play (max 15)</li>
<li>Do-not-play</li>
<li>Prvý tanec + rodinné tance</li>
<li>Časový harmonogram</li>
<li>Špeciálne momenty</li>
</ol>

<p>S týmto briefom zvládne dobrý DJ zvyšok. A vy si môžete užívať večer namiesto riadenia playlistu z mobilu.</p>
