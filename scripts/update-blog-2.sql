<p>Dobrý svadobný DJ nie je len „ten, čo púšťa pesničky“. Je to človek, ktorý drží tempo večera, vie čítať parket, zvláda prechody medzi ceremoniálom, večerou a tanečnou párty – a pritom pôsobí nenápadne, kým ho nepotrebujete.</p>

<p>Zlý výber sa prejaví rýchlo: príliš hlasná večera, prázdny parket, nefunkčný mikrofón pri príhovore alebo playlist, ktorý baví len jednu generáciu. Preto si dajte na výber čas rovnako ako na fotografa.</p>

