<h2>1. Skúsenosti so svadbami, nie len s klubmi</h2>
<p>Opýtajte sa, koľko svadieb robil za posledný rok a či vie moderovať (príchod snúbencov, prvý tanec, súťaže, torta). Klubový set a svadobný večer sú iný žáner. Na svadbe musí zvládať rodinnú atmosféru aj nočný peak.</p>

<h2>2. Referencie a ukážky</h2>
<p>Požiadajte o krátke video z reálnej svadby, ukážku playlistu a kontakty na páry. Overení DJ-i na platformách (vrátane BookTheVibe) majú verejný profil, fotky, galériu a recenzie – to je dobrý filter ešte pred prvým hovorom.</p>

<h2>3. Technika v cene</h2>
<ul>
<li>Ozvučenie vhodné pre veľkosť sály</li>
<li>Základné svetlá / ambient</li>
<li>Mikrofón na príhovory</li>
<li>Záloha (spare) pri kritických zariadeniach</li>
</ul>
<p>Vždy si overte, či je technika v cene, alebo sa pripočítava zvlášť – a kto rieši prúd, káble a miesto na setup. Sála bez dostatočného prúdu vie „zabíť“ aj najlepší set.</p>

<figure><img src="https://images.unsplash.com/photo-1470225620780-dba8ba36b745?auto=format&fit=crop&w=1200&q=80" alt="Hudobná produkcia" /><figcaption>Dobré ozvučenie a mikrofón pri príhovoroch sú non-negotiable.</figcaption></figure>

<h2>4. Komunikácia pred svadbou</h2>
<p>Profík si vypýta: must-play / do-not-play zoznam, časový harmonogram, vekové zloženie hostí a či bude kapela alebo spev pred DJ setom. Ak nereaguje na správy do pár dní ešte pred rezerváciou, počítajte s tým istým stresom aj v týždni pred svadbou.</p>

<h2>5. Zmluva a záloha</h2>
<p>V zmluve majte dátum, miesto, čas hrania, čo je v cene, cestovné, storno podmienky a či DJ môže hrať do rána. Záloha je bežná – trvajte na písomnom potvrdení termínu. Bez zmluvy riskujete, že „voľný termín“ zmizne.</p>

<h2>6. Osobné stretnutie (aspoň online)</h2>
<p>Chémia je dôležitá. DJ bude s vami celý večer – má pôsobiť pokojne, flexibilne a profesionálne, nie tlačiť „svoj“ štýl za každú cenu. Ak vám je nepríjemný už na call-e, na svadbe to lepšie nebude.</p>

<h2>Rýchly checklist pred podpisom</h2>
<ol>
<li>Máte video / referencie zo svadby?</li>
<li>Je jasné, čo je v cene (zvuk, svetlá, mikrofón, hodiny)?</li>
<li>Dostali ste návrh zmluvy?</li>
<li>Vie pracovať s vaším timeline?</li>
<li>Máte rezervovaný termín písomne?</li>
</ol>

