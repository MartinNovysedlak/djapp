<p>Svadba má stovky detailov. Väčšina z nich je milá, ale nie kritická. Týchto desať bodov však rozhoduje o tom, či večer pôjde hladko – alebo budete hasiť problémy namiesto tanca.</p>

<p>Berte ich ako kontrolný zoznam 4–8 týždňov pred dátumom. Čím skôr ich uzavriete, tým pokojnejší bude posledný týždeň.</p>

<figure><img src="https://images.unsplash.com/photo-1520854221256-17451cc331bf?auto=format&fit=crop&w=1200&q=80" alt="Svadobný pár" /><figcaption>Najlepší „detail“ je pokojný pár – zvyšok sa dá naplánovať.</figcaption></figure>

<h2>1. Realistický rozpočet s rezervou</h2>
<p>Pridajte 10–15 % rezervu na nečakané (korkovné, doprava, neskôr objednané detaily, predĺženie DJ-a). Bez rezervy každý „malý“ výdavok bolí a vytvára stres medzi vami a rodičmi.</p>

<h2>2. Časový harmonogram dňa</h2>
<p>Napíšte si timeline: príprava, obrad, fotenie, príchod, večera, prvý tanec, torta, voľná zábava. Pošlite ho DJ-ovi, fotografovi aj miestu konania. Keď každý vie, čo bude o 20:30, odpadá polovica chaosu.</p>

<h2>3. Zodpovedná osoba (nie snúbenci)</h2>
<p>Určte koordinátora alebo blízkeho, kto rieši dodávateľov počas dňa. Vy máte oslavovať, nie telefonovať catering, že chýba vegetariánske menu.</p>

<h2>4. Zoznam hostí a seating</h2>
<p>Finálny počet hostí ovplyvní catering, tortu aj priestor. Seating chart ušetrí chaos pri večeri a pomôže aj DJ-ovi / fotografovi vedieť, kde sedia kľúčoví ľudia.</p>

<figure><img src="https://images.unsplash.com/photo-1519225421980-715cb0215aed?auto=format&fit=crop&w=1200&q=80" alt="Svadobná výzdoba" /><figcaption>Detaily majú zmysel až vtedy, keď funguje základ: čas, ľudia, jedlo, hudba.</figcaption></figure>

<h2>5. Foto a video s briefom</h2>
<p>Napíšte must-have momenty (prvý tanec, rodičia, kľúčoví hostia, grupáky). Bez briefu sa ľahko stane, že chýba práve tá fotka, na ktorej vám záleží.</p>

<h2>6. Hudba s must / don’t play listom</h2>
<p>Dajte DJ-ovi 10–20 skladieb, ktoré chcete, a zoznam, ktorý nechcete. Nechajte mu priestor reagovať na parket – príliš dlhý povinný zoznam parkiet zabíja.</p>

<h2>7. Technika a ozvučenie na príhovory</h2>
<p>Mikrofón a fungujúci zvuk pri príhovoroch sú základ. Overte to pri obhliadke sály. Nič nerozbije atmosféru rýchlejšie než „počuť vás nie je“.</p>

<h2>8. Plán B pri počasí</h2>
<p>Pri outdoor obrade majte záložný priestor alebo stan. Rozhodnite sa vopred, kto rozhoduje o zmene (nečakajte do poslednej minúty a nehádajte sa pred hosťami).</p>

<h2>9. Jedlo a alergény</h2>
<p>Opýtajte sa hostí na alergie a vegetariánske / bezlepkové menu. Catering to potrebuje včas – last minute zmeny sú drahé a stresujúce.</p>

<h2>10. Čas pre seba</h2>
<p>Vložte do harmonogramu 10–15 minút len pre vás dvoch. Svadba ubehne rýchlo – tieto minúty si zapamätáte viac než dokonalú mašľu na servítke.</p>

<p>Ak máte tieto body pokryté, zvyšok je už len atmosféra – a tú najlepšie doladí dobrý DJ, kvalitné foto a hostia, ktorých ste pozvali zámerne, nie zo zvyku.</p>
